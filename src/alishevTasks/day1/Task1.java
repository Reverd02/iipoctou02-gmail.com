package src.alishevTasks.day1;

/**
 * Вывести на экран слово “JAVA” в строку 10 раз, используя цикл while
 */
public class Task1 {
    public static void main(String[] args) {
        int count = 0;
        while (count < 9) {
            System.out.print("JAVA ");
            count++;
        }
        System.out.print("JAVA");
    }
}
